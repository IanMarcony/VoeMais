    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        String email = this.emailTextField.getText();
        String password =  new String(this.passwordTextField.getPassword());
        if(!email.contains("@")&&!email.equals("admin")){
            JOptionPane.showMessageDialog(rootPane, "Digite um email v√°lido!");
                return;
        }
        
        if(email.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!");
                return;
        }
        
        
        if(email.equals("admin")&&password.equals("admin")){
            Employee employee = new Employee("admin", password, email);
            Employee employeeAlreadyExists =  EmployeeController.getFromDB(employee);
            
            if(employeeAlreadyExists==null){
                SerializableManager.saveObject(employee.getName(), employee);
            }           
            
            SelectionMenuFrame dados = new SelectionMenuFrame();
            
            dados.pack();
            dados.show();
            this.dispose();
        }else if(email.equals("admin")&&!password.equals("admin")){
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente ADMIN!");
                return;
        }else{
            Client client = new Client("1223554488", "MASC", "Fulano de tal", password, email);
            Client clientAlreadyExists =  ClientController.getFromDB(client);
            
            if(clientAlreadyExists==null){
                SerializableManager.saveObject(client.getName(), client);
                clientAlreadyExists = client;
            }         
            
            Client.setInstance(clientAlreadyExists); 
            
            
            ClientMenuJFrame dados = new ClientMenuJFrame();
            
            dados.pack();
            dados.show();
            this.dispose();
        }
        
        
        
    }//GEN-LAST:event_signInButtonActionPerformed

